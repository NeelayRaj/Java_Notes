package arrays;

public class TwoDArrays {

	public static void main(String[] args) {
		int a [] [] = new int [5] [3];
		
		System.out.println(a[2][1]);
		
		int b [][] = {
				{1,2,3,4,8},
				{1,2,5},
				{1,7,0,7,8,6,7},
				{1,4,8,9}
		};
		System.out.println(b[2][4]);
		System.out.println(b[3][3]);
	}

}
