public class ComparisonOperator {
	public static void main(String[] args) {
		
		Scanner Compa = new Scanner(System.in); // Scanner Compa defined
		int a = 3;
		int b = 4;
		int c = Compa.nextInt(); // for user input
		int d = Compa.nextInt(); // for user input
		
		System.out.println(b < a); // less than
		System.out.println(d >= c); // less than equal to
		System.out.println(d == c); // equal to comparison
		System.out.println(a != b); // not equal to comparison
		// Comparision Operator
		
		
	}

}
